/*旋转数组
给定一个数组，将数组中的元素向右移动 k 个位置，其中 k 是非负数。
示例 1:

输入: [1,2,3,4,5,6,7] 和 k = 3
输出: [5,6,7,1,2,3,4]
解释:
向右旋转 1 步: [7,1,2,3,4,5,6]
向右旋转 2 步: [6,7,1,2,3,4,5]
向右旋转 3 步: [5,6,7,1,2,3,4]
示例 2:

输入: [-1,-100,3,99] 和 k = 2
输出: [3,99,-1,-100]
解释:
向右旋转 1 步: [99,-1,-100,3]
向右旋转 2 步: [3,99,-1,-100]
说明:

尽可能想出更多的解决方案，至少有三种不同的方法可以解决这个问题。
要求使用空间复杂度为 O(1) 的原地算法。*/


/*思路：以k为分界线，将数组分为前0 --> numsSize -1-k,和numsSize -k -->numsSize -1 两部分
先将前半部分逆置，再将后半部分逆置，最后将整个数组逆置。*/

//代码如下：
void rotate(int* nums, int numsSize, int k){
	int i = 0;
	int tmp = 0;
	int j = 0;
	k %= numsSize;//只需要旋转比numsSize小的长度
	for (i = 0, j = numsSize - 1 - k; i<j; i++, j--)//对前半部分旋转
	{
		tmp = nums[i];
		nums[i] = nums[j];
		nums[j] = tmp;
	}
	for (i = numsSize - k, j = numsSize - 1; i<j; i++, j--)//对后半部分旋转
	{
		tmp = nums[i];
		nums[i] = nums[j];
		nums[j] = tmp;
	}
	for (i = 0, j = numsSize - 1; i<j; i++, j--)//对整个数组旋转
	{
		tmp = nums[i];
		nums[i] = nums[j];
		nums[j] = tmp;
	}

}